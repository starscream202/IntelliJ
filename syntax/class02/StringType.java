package com.syntax.class02;

public class StringType {
	public static void main(String[] args) {
		String name="Olga";
		System.out.println("my name is " +name);
		String last="Petrov";
		System.out.println(name+" "+last);
		String city="Miami";
		System.out.println("Olga lives in"+" "+city);
		char grade='B';
		System.out.println(grade+" is your grade "+name);
		int age=21;
		System.out.println(age+" is your number "+name);
		//to attach any value to a string we use + sign called (string concatenation operator)
	}
}
