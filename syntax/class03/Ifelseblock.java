package com.syntax.class03;

public class Ifelseblock {
	public static void main(String[] args) {
		double money = 5;
		double iceCream = 4.59;
		if (iceCream < money) {
			System.out.println("I will buy ice cream.");
		} else {
			System.out.println("I can't buy ice cream.");
		}
		System.out.println("I am code without any conditions.");
	}

}
