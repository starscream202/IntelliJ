package com.syntax.class03;

public class Operators {
	public static void main(String[] args) {
		
		int num=100;
		num=num+10;
		System.out.println(num);
		
		num=num+200;
		System.out.println(num);
		//compound operators 
		int myNumber=100;
		myNumber+=100;
		int num2=200;
		num2-=67;
		int cakepiece=11;
		cakepiece/=4;
		cakepiece=25;
		cakepiece%=7;
		System.out.println(cakepiece);
		
	
}

}
